---- SCENARIO:3------------AVOIDIDING MULTIPLE TO BE SCHEDULE AT SAME TIME FOR DIFFEERENT TASK  

CREATE TRIGGER TRG_CHECKMACHINESCHEDULE
ON PRODUCTIONORDERS 
AFTER INSERT ,UPDATE 
AS 
BEGIN
    IF EXISTS (
         SELECT 1
         FROM INSERTED AS I 
         JOIN PRODUCTIONORDERS AS P
         -- MUST BE FOR SAME MACHINE 
         ON I.ASSIGNEDMACHINEID=P.ASSIGNEDMACHINEID
         --MUST NOT BE EXACT SAME ORDER (HANDLES SELF UPDATES )
         AND I.PRODUCTIONID<>P.PRODUCTIONID
       WHERE 
           I.STATUS NOT IN ('COMPLETED','CANCELLED')


           --- THIS THIS THE OVERLAP LOGIC 
           --1 THE NW=E W ORDERS START IS BEFORE THE EXISTNG ORDER END 
           AND (I.SCHEDULEDSTARTDATE<P.SCHEDULEENDDATE)

           --2 AND THE NEW ORDER EBD IS AFTER EXISTING ORDERS START 
           AND I.SCHEDULEENDDATE>P.SCHEDULEDSTARTDATE

       )

       BEGIN 
       -- IF CONFLICT IS FOUND, RAISE AN ERROR AND CANCEL THE OPERATION 
       RAISERROR ('SCHEDULE CONFLICT : THE ASSIGNED MACHINE IS ALREADY OCCUPIED DURING THIS TIME PERIOSD ',16,1)
       ROLLBACK TRANSACTION ;
       RETURN;
END;
END;


