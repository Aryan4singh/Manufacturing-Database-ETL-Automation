--ANY UPDATION IN MY PRODUCTION MATERIAL USED TABLE SHOULD MAKE RELATIVE CHANGES IN THE INVENTORY TABLE
-- ANY ROW INSERTED INTO PRODUCTION MATERIAL USAGE SHOULD SHOW RELATIVE CHANGES IN THE REMAINING QUANTITY COLUMN OF THE TABLE MATERIAL INVENTORY
--ANY UPDATION IN ALREADY EXISTING MATERIAL USAGE SHOULD SHOW RELATIVE CHANGES IN THE REMAINING QUANTITY COLUMN
-- IF QUANNTIY USED IS MORE THAN QUANTITY REMAINNIG IN INVENTORY TABLE DONT ALLOW ENTRY OF THAT ROW ,OR UPDATION OF THAT ROW

CREATE TRIGGER TRG_UPDATEMATERIALREMAINING
ON PRODUCTIONMATERIALUSAGE
AFTER INSERT ,UPDATE 
AS
BEGIN   ---UPDATE
        -- DELETE
        IF EXISTS(SELECT 1 FROM DELETED)
        BEGIN
            UPDATE MI
            SET MI.REMAINING_QUANTITY=MI.REMAINING_QUANTITY+D.QUANTITY_USED
            FROM  DELETED AS D INNER JOIN
            MATERIALINVENTORY AS MI 
            ON D.BATCHID=MI.BATCHID AND D.MATERIALID=MI.MATERIALID
          END


          ---INSERT
          UPDATE MI
          SET MI.REMAINING_QUANTITY=MI.REMAINING_QUANTITY-I.QUANTITY_USED
          FROM MATERIALINVENTORY AS MI 
          INNER JOIN INSERTED AS I
          ON I.BATCHID=MI.BATCHID AND I.MATERIALID=MI.MATERIALID

          ---
          IF EXISTS (SELECT 1
          FROM MATERIALINVENTORY AS MI 
          INNER JOIN INSERTED AS I
          ON I.BATCHID=MI.BATCHID AND I.MATERIALID=MI.MATERIALID
          WHERE MI.REMAINING_QUANTITY<0)
          BEGIN 
             RAISERROR('INSUFFICIENT MATERIAL',16,1)
             ROLLBACK TRANSACTION 
