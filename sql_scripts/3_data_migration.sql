-- 7. POPULATE CONTROL FACT ELEMENTS (QUALITY AUDITS)

INSERT INTO QUALITYCHECKS (ORIGINALQUALITYCHECKID, PRODUCTIONORDERID, INSPECTORID, CHECKTIMESTAMP, RESULT)
SELECT 
    D.QualityCheckID, P.PRODUCTIONID, E.EMPLOYEEID, 
    TRY_CONVERT(DATETIME, D.CheckTimestamp, 105), D.Result 
FROM DUMMY AS D
INNER JOIN PRODUCTIONORDERS AS P ON D.ProductionOrderID = P.PRODUCTIONCODE 
    AND D.ScheduledStart = P.SCHEDULEDSTARTDATE AND D.ScheduledEnd = P.SCHEDULEENDDATE 
    AND D.QuantityToProduce = P.QUANTITYTOPRODUCE AND D.ComponentToProduce = P.COMPONENTTOPRODUCE 
INNER JOIN EMPLOYEES AS E ON E.FULLNAME = D.InspectorName 
WHERE D.QualityCheckID IS NOT NULL;

-- 8. INITIALIZE POST-MIGRATION HISTORICAL REMAINING VALUES

UPDATE MI
SET MI.REMAINING_QUANTITY = MI.INITIAL_QUANTITY - ISNULL(P.QUANTITY_USED, 0)
FROM MATERIALINVENTORY AS MI
LEFT JOIN PRODUCTIONMATERIALUSAGE AS P ON P.BATCHID = MI.BATCHID AND P.MATERIALID = MI.MATERIALID;

UPDATE MATERIALINVENTORY
SET REMAINING_QUANTITY = INITIAL_QUANTITY WHERE REMAINING_QUANTITY IS NULL;
